

a = 10
b = 3

suma = a + b

print('suma: {}'.format(suma))

resta = a - b

print(f'Resta: {resta}')

multiplicacion = a * b
print(f'Multiplicacion: {multiplicacion}')

division = a / b #devuelve un float
print(f'Division: {division}')

division_entera = a // b
print(f'Division entera: {division_entera}')

#residuo de la division
modulo = a % b
print(f'Residuo o modulo de la division: {modulo}')

#potenciacion
exponente = a ** b  #10 elevado a la 3 o 10*10*10
print(f'Exponente: {exponente}')