print('*** Operadores de asignacion compuestos ***')
a, b = 10, 15
print(f'Valor inicial a: {a}, b: {b}')

#operador de +=
a += b # a = a + b
print(f'Operador a += b es: {a}')

#operador compuesto de resta -=
a= 10
a -= b # a = a - b
print(f'Operador a -= b es: {a}')

#operador *=
a = 10
a *= b
print(f'operador a*= b es: {a}')

#operador division /=
a = 10
a /= b
print(f'operador a/= b es: {a:.2f}')

