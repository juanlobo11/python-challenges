#operadores de comparacion o relacionales

a, b = 10, 6
print(f'Valor inicial a: {a}, b: {b}')

#operador igualdad
resultado = a == b
print(f'Resultado a == b es: {resultado}')

#operador diferente !=
resultado = a != b
print(f'Resultado a != b es: {resultado}')

#operador mayor que>
resultado = a > b
print(f'Resultado a > b es: {resultado}')

# operador menor que <
resultado = a < b
print(f'Resultado a < b es: {resultado}')

#operador menor o igual que
resultado = a <= b
print(f'Resultado a <= b es: {resultado}')

#operador mayor o igual que
resultado = a >= b
print(f'Resultado a >= b es: {resultado}')